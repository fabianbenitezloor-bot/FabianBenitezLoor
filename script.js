function detectDevice(){
  const ua = navigator.userAgent || '';
  const isTablet = /iPad|Tablet|PlayBook|Silk/i.test(ua) || (navigator.maxTouchPoints > 1 && Math.min(window.innerWidth, window.innerHeight) >= 700);
  const isMobile = !isTablet && (/Mobi|Android|iPhone|iPod/i.test(ua) || window.innerWidth < 700);
  const device = isMobile ? 'mobile' : isTablet ? 'tablet' : 'desktop';
  document.body.classList.remove('device-mobile','device-tablet','device-desktop');
  document.body.classList.add(`device-${device}`);
  document.documentElement.dataset.device = device;
  return device;
}
function detectLanguage(){
  const languages = (navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language || 'en']).map(l => String(l).toLowerCase());
  const primary = languages[0] || 'en';
  document.documentElement.dataset.detectedLanguage = primary;
  if (languages.some(l => l.startsWith('es'))) return 'es';
  if (languages.some(l => l.startsWith('en'))) return 'en';
  return 'en';
}
